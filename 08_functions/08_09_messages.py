# 8-9. Messsages

# Make a list containing a series of short text messages.
texts = ['Hi.', 'How are you?', 'How have you been?', 'I love you.']

# Pass the list to a function called show_messages(), which prints each text msg.
def show_messages(texts):
    for text in texts:
        print(text)
show_messages(texts)