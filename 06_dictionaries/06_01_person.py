# 6-1. Person
# Use a dictionary to store information about a person I know.

# Store her first name, last name, age and the city in which she lives.
friend = {'first_name': 'Emily', 'last_name': 'Ma', 'age': '22', 'city': 'Dallas'}

# Print each piece of information stored in my dictionary.
print(f"\nHer first name is {friend['first_name']}.")
print(f"\nHer last name is {friend['last_name']}.")
print(f"\nHer age is {friend['age']}.")
print(f"\nShe lives in {friend['city']}.")